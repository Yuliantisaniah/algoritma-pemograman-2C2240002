#include <iostream>
using namespace std;

int main() {
    int nilai_awal, nilai_akhir;
    
    // Meminta input dari user untuk nilai a dan b
    cout << "Masukkan nilai a: ";
    cin >> nilai_awal;
    cout << "Masukkan nilai b: ";
    cin >> nilai_akhir;

    // Perulangan dari a hingga b
    for (int i = nilai_awal; i <= nilai_akhir; i++) {
        cout << i << ".classyscience.ui" << endl;  // Menampilkan nilai i
    }

    return 0;
}

